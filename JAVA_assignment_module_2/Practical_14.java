package JAVA_assignment_module_2;
/*
 * W.A.J.P to get the character at the given index within the String. 
 * Original String = Tops Technologies! The character at position 0 is T, 
 * The character at position 10 is o
 */
public class Practical_14 
{
	public static void main(String[] args)
	{
        String s1="Tops Technologies";
        System.out.println("String is:"+s1);
        System.out.println("character at index 0:"+s1.charAt(0));
        System.out.println("character at index 0:"+s1.charAt(10));
    
	}
}
